function clearMap() {
  markersLayer.clearLayers();
  if (routeLine) map.removeLayer(routeLine);
  if (routeMarker) map.removeLayer(routeMarker);
  if (animationTimer) clearInterval(animationTimer);
  routeLine = null;
  routeMarker = null;
  animationTimer = null;
}

function renderStops(stops) {
  stopsList.innerHTML = "";
  stops.forEach((stop, idx) => {
    const li = document.createElement("li");
    li.textContent = stop;
    li.style.transitionDelay = `${idx * 40}ms`;
    stopsList.appendChild(li);
  });
}

function filterStops() {
  const q = stopSearch.value.trim().toLowerCase();
  [...stopsList.children].forEach(li => {
    li.style.display = li.textContent.toLowerCase().includes(q) ? "" : "none";
  });
}

function animateBus(coords) {
  routeMarker = L.marker(coords[0], { icon: createBusIcon() }).addTo(map);
  let i = 0;
  animationTimer = setInterval(() => {
    i = Math.min(i + 1, coords.length - 1);
    routeMarker.setLatLng(coords[i]);
    if (i >= coords.length - 1) clearInterval(animationTimer);
  }, 650);
}

function fillStopsData() {
  const uniqueStops = new Set();
  Object.values(data).forEach(routes => {
    routes.forEach(route => route.stops.forEach(stop => uniqueStops.add(stop)));
  });
  stopsData.innerHTML = "";
  [...uniqueStops].sort().forEach(stop => {
    const option = document.createElement("option");
    option.value = stop;
    stopsData.appendChild(option);
  });
}

function showRoute(route) {
  routePanel.classList.add("active");
  busCard.classList.add("show");

  routeTitle.textContent = `Маршрут ${route.number}`;
  routeTime.textContent = route.time;
  routeNumber.textContent = route.number;
  busImage.src = route.image;
  busLabel.textContent = `Маршрут ${route.number}`;
  busSub.textContent = `Остановок: ${route.stops.length}`;

  stopSearch.value = "";
  renderStops(route.stops);
  clearMap();

  const latlngs = route.coords;
  routeLine = L.polyline(latlngs, {
    color: "#ffd700",
    weight: 5,
    opacity: 0.9
  }).addTo(map);

  latlngs.forEach((c, idx) => {
    const m = L.circleMarker(c, {
      radius: 7,
      color: "#ffd700",
      weight: 2,
      fillColor: "#111",
      fillOpacity: 1
    }).addTo(markersLayer);
    m.bindPopup(route.stops[idx]);
  });

  map.fitBounds(routeLine.getBounds().pad(0.18));
  animateBus(latlngs);
}

function loadRoutes() {
  routeSelect.innerHTML = "";
  const city = citySelect.value;
  data[city].forEach(r => routeSelect.add(new Option(`Маршрут ${r.number}`, r.number)));
  showRoute(data[city][0]);
}

function sameStop(a, b) {
  return a.trim().toLowerCase() === b.trim().toLowerCase();
}

function findBestRoute(from, to) {
  const city = citySelect.value;
  const routes = data[city];

  for (const route of routes) {
    const a = route.stops.findIndex(s => sameStop(s, from));
    const b = route.stops.findIndex(s => sameStop(s, to));
    if (a !== -1 && b !== -1 && a !== b) {
      return { route, a, b };
    }
  }

  for (const route of routes) {
    const a = route.stops.findIndex(s => s.toLowerCase().includes(from.trim().toLowerCase()));
    const b = route.stops.findIndex(s => s.toLowerCase().includes(to.trim().toLowerCase()));
    if (a !== -1 && b !== -1 && a !== b) {
      return { route, a, b };
    }
  }

  return null;
}

function markPickedPoint() {
  if (!pickedPoint) return;
  if (pickedMarker) map.removeLayer(pickedMarker);

  pickedMarker = L.marker(pickedPoint, {
    icon: L.divIcon({
      className: "",
      html: `<div style="
        width:18px;height:18px;border-radius:50%;
        background:#ffd700;border:3px solid #111;
        box-shadow:0 0 0 6px rgba(255,215,0,.15)
      "></div>`,
      iconSize: [18, 18],
      iconAnchor: [9, 9]
    })
  }).addTo(map);
}

