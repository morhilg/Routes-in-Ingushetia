findRouteBtn.addEventListener("click", () => {
  const from = fromInput.value.trim();
  const to = toInput.value.trim();

  if (!from || !to) {
    tripResult.textContent = "Заполни обе строки: «откуда» и «куда».";
    tripResult.classList.remove("match-found");
    tripResult.classList.add("match-notfound");
    return;
  }

  const result = findBestRoute(from, to);

  if (!result) {
    tripResult.textContent = "Подходящий маршрут не найден. Попробуй выбрать остановки точнее.";
    tripResult.classList.remove("match-found");
    tripResult.classList.add("match-notfound");
    return;
  }

  const { route, a, b } = result;
  routeSelect.value = route.number;
  showRoute(route);

  tripResult.textContent = `Подходит маршрут ${route.number}. Отрезок: ${route.stops[a]} → ${route.stops[b]}.`;
  tripResult.classList.remove("match-notfound");
  tripResult.classList.add("match-found");

  const fromLat = route.coords[a];
  const toLat = route.coords[b];
  const bounds = L.latLngBounds([fromLat, toLat]);
  map.fitBounds(bounds.pad(0.35));
});

